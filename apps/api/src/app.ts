import express, {
  json,
  urlencoded,
  Express,
  Request,
  Response,
  NextFunction,
  Router,
} from 'express';
import cors from 'cors';
import { PORT } from './config';
import { PersonnelRouter } from './routers/personnel.router';
import { ClientRouter } from './routers/client.router';
import { ProductRouter } from './routers/product.router';
import { InvoiceRouter } from './routers/invoice.router';
import path from 'path';

export default class App {
  private app: Express;

  constructor() {
    this.app = express();
    this.configure();
    this.routes();
    this.handleError();
  }

  private configure(): void {
    this.app.use(cors());
    this.app.use(json());
    this.app.use(urlencoded({ extended: true }));
    this.app.use('/storage', express.static(path.join(__dirname, 'storage')));
  }

  private handleError(): void {
    // not found
    this.app.use((req: Request, res: Response, next: NextFunction) => {
      if (req.path.includes('/api/')) {
        res.status(404).send('Not found !');
      } else {
        next();
      }
    });

    // error
    this.app.use(
      (err: Error, req: Request, res: Response, next: NextFunction) => {
        if (req.path.includes('/api/')) {
          console.error('Error : ', err.stack);
          res.status(500).send('Error !');
        } else {
          next();
        }
      },
    );
  }

  private routes(): void {
    const personnelRouter = new PersonnelRouter();
    const clientRouter = new ClientRouter();
    const productRouter = new ProductRouter();
    const invoiceRouter = new InvoiceRouter();

    this.app.get('/api', (req: Request, res: Response) => {
      res.send(`Hello, Purwadhika Student API!`);
    });
    this.app.use('/api/personnel', personnelRouter.getRouter());
    this.app.use('/api/client', clientRouter.getRouter());
    this.app.use('/api/product', productRouter.getRouter());
    this.app.use('/api/invoice', invoiceRouter.getRouter());
  }

  public start(): void {
    this.app.listen(PORT, () => {
      console.log(`  âžœ  [API] Local:   http://localhost:${PORT}/`);
    });
  }
}
